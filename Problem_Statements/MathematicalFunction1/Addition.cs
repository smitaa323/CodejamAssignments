using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathematicalFunction1
{
    public class AdditionMultiplication
    {
        public int AddTwoValues(int x, int y)
        {
            return x + y;
        }

        public int AddThreeValues(int x, int y, int z)
        {
            return x + y + z;

        }
        public int MultiplyThreeValue(int x,int y,int z)
        {
            return x * y * z;
        }
    }
  


}
